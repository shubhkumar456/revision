package loops;

public class WhileLoop {

    public static void main(String[] args) {

        int i =0;
        while (i<10){
            System.out.println("In while loop :-"+i);
            i++;
        }

        int x =10;
        while(x>0){
            System.out.println("decreent of while loop :"+x);
            x--;
        }

    }
}
